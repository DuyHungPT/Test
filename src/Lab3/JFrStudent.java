    private void txtMarkCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txtMarkCaretUpdate
        // TODO add your handling code here:
        if(!txtMark.getText().isEmpty()){
          double mark = Double.parseDouble(txtMark.getText());
        stu.setMark(mark);
        txtHocLuc.setText(stu.getHocLuc());   
        }
        
    }//GEN-LAST:event_txtMarkCaretUpdate
    public Student getModel(){
        stu = new Student();
        stu.setId(txtID.getText());
        stu.setName(txtName.getText());
       stu.setMark(Double.parseDouble(txtMark.getText()));
        stu.setMajor(cboChuyenNganh.getSelectedItem().toString());
        return stu;
    }
    public void setForm(){
        btnADD.setEnabled(false);
        btnDel.setEnabled(false);
        btnUpdate.setEnabled(false);
        
    }
    public void resetForm(){
              btnADD.setEnabled(true);
        btnDel.setEnabled(true);
        btnUpdate.setEnabled(true); 
    }
    private void btnADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnADDActionPerformed
        // TODO add your handling code here:
        int rs = dao.add(getModel());
        if(rs >= 0){
            JOptionPane.showMessageDialog(this, "Them Thanh Cong");
        }
        fillTable();
        resetDataForm();
        setForm();
    }//GEN-LAST:event_btnADDActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        // TODO add your handling code here:
        if(txtID.getText().isEmpty()){
            int rs = dao.findStudentByID(txtID.getText());
            if(rs >= 0){
                dao.del(txtID.getText());
                JOptionPane.showMessageDialog(this, "Da xoa thanh cong");
            }
        }
    }//GEN-LAST:event_btnDelActionPerformed
    public void setModel(Student st){
        txtID.setText(st.getId());
        txtName.setText(st.getName());
        txtMark.setText(st.getMark()+"");
        cboChuyenNganh.setSelectedItem(st.getMajor());
    }
