    public void fillTable(){
        DefaultTableModel model = (DefaultTableModel)tbStudent.getModel();
        model.setRowCount(0);
        for (Student student : dao.getAll()){
            Object[] rowdata = new Object[6];
            rowdata[0] = student.getId();
            rowdata[1] = student.getName();
            rowdata[2] = student.getMark();
            rowdata[3] = student.getMajor();
            rowdata[4] = student.getHocLuc();
             rowdata[5] = student.isHocBong()?"Khen Thuong": "Khong";
             model.addRow(rowdata);
        }
    }

